#include <unistd.h>

char rotone(char c)
{
	if ((c >= 'a' && c <= 'y') || (c >= 'A' && c <= 'Y'))
		c = c + 1;
	else if (c == 'z' || c == 'Z')
		c = c - 25;
	return (c);
}			
			
int	main(int ac, char **av)
{
	int i;
	char p;

	i = 0;
	if (ac == 2)
	{
		while (av[1][i])
		{
			p = rotone(av[1][i]);	
			write (1, &p, 1);
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);	
}

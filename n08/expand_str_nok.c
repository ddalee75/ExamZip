#include <unistd.h>

void	ft_expand_str(char *s)
{
	int	i;
	
	i = 0;
	while (s[i]) 
	{
		while (s[i] && (s[i] == ' ' || s[i] == '\t'))
			i++;
		while (s[i] && (s[i] != ' ' && s[i] != '\t'))
		{
			write (1, &s[i], 1);
			i++;
		}
		if (s[i] && (s[i] == ' ' ||  s[i] == '\t') 
			&& (s[i - 1] != ' ' && s[i -1] != '\t'))
			write (1, "   ", 3);
		}
}

int	main(int ac, char **av)
{
	if (ac == 2)
	{
		ft_expand_str(av[1]);
	}
	write (1, "\n", 1);
	return (0);
}
